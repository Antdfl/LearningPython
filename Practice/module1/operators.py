# Operators
# Addition: +
# Subtraction: -
# Multiplication: *
# Division: /
# Modulus(remainder): %
# Exponentiation: **
# Floor Division: //
# two ints return int
# two floats return float
# int and float return float

# Equal to: ==
# Not equal to: !=
# Greater than: >
# Less than: <
# Greater than or equal to: >=
# Less than or equal to: <=

# Logical Operators
# logical AND: and
# logical OR: or
# logical NOT: not

# Assignment operators
# Assign: :=
# Add and assign: +=
# Subtract and assign: -=
# Multiply and assign: *=
# Divide and assign: /=
# Modulus and assign: %=

# Bitwise operators
# Bitwise AND: &
# Bitwise OR: |
# Bitwise NOT: ~
# Bitwise XOR: ^
# Bitwise left shift: <<
# Bitwise right shift: >>
# We use these operators to perform bit-level operations on integers.
# for example, image pixel manipulation, permission control,
# performance optimization, cryptography, low-level programming,
# hardware communication, and more.

# Membership operators
# in
# not in

# Operator priority
# 1. Parentheses: ()
# 2. Exponentiation: **
# 3. Unary plus/minus: +x, -x
# 4. Multiplication/Division/Modulus: *, /, %
# 5. Addition/Subtraction: +, -
# 6. Relational:  ==, !=, >, <, >=, <=
# 7. Bitwise NOT: ~
# 8. Bitwise AND: &
# 9. Bitwise XOR: ^
# 10. Bitwise OR: |
# 11. Assignment: =, +=, -=, *=, /=, %=, **=, //=
# All these operators follow the associativity rules:
# Most operators are left-to-right associative
# we use this when there is a tie in a formula